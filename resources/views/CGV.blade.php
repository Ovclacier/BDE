</p>
<p>
    Produits :
</p>
<p>
    Les produits proposés sont issus d’achats effectués au préalable auprès de
    commerces et particuliers locaux
</p>
<p>
    Prix :
</p>
<p>
    Les prix sont indiqués en euros TTC hors frais d’envoi, les prix en vigueur
    au moment de la vente seront ceux appliqué a l’achat du produit.
</p>
<p>
    Les commandes :
</p>
<p>
    Le processus de commande comprend plusieurs étapes :
</p>
<p>
    1) L’internaute sélectionne sur le site les produits qu’il choisit et
    indique la quantité désirée
</p>
<p>
    2) Un récapitulatif de sa commande lui est proposé et l’internaute a la
    possibilité de rectifier sa commande
</p>
<p>
    3) L’internaute valide sa commande : en confirmant la commande,
    l’internaute reconnait son obligation de paiement
</p>
<p>
    4) Une fois la commande validée, l’association Bureaux des Etudiants Cesi
    Strasbourg envoie dans les meilleurs délais un accusé de réception de la
    commande.
</p>
<p>
    Paiement :
</p>
<p>
    Le paiement en ligne peut se faire par Paypal; vos informations de paiement
    ne seront pas conservées après le paiement.
</p>
<p>
    Livraison :
</p>
<p>
    Nous ne pouvons assurer l’envoi des produits proposés. La livraison se fera
    lors d’un rendez-vous avec un membre de notre équipe à l’endroit et l’heure
    indiquée dans le contenu d’un e-mail envoyé à l’acheteur lors de sa
    commande. Dans la mesure du possible, nous procédons à un envoi groupé. Si
    cela n’est pas possible, les frais de livraison des envois complémentaires
    sont à la charge de l’association.
</p>
<p>
    Droit de rétractation :
</p>
<p>
    En application de l’article L121-17 I du code de la consommation, vous avez
    le droit de vous rétracter de la présente vente, sans donner de motif, dans
    un délai de quatorze jours.
</p>
<p>
    Le délai de rétractation expire quatorze jours après le jour où vous-même,
    ou un tiers autre que le transporteur et désigné par vous, prend
    physiquement possession du bien (ou du dernier bien si plusieurs colis
    issus de la même commande sont nécessaires).
</p>
<p>
    Pour exercer le droit de rétractation, vous devez nous notifier à
    BDE-Strasbourg@cesi.fr votre décision de rétractation, au moyen d'une
    déclaration dénuée d'ambiguïté (par exemple, lettre envoyée par la poste,
    télécopie ou courrier électronique).
</p>
<p>
    Pour que le délai de rétractation soit respecté, il suffit que vous
    transmettiez votre décision de rétractation avant l'expiration du délai de
    rétractation.
</p>
<p>
    Effets de rétractation :
</p>
<p>
    En cas de rétractation de votre part, nous vous rembourserons tous les
    paiements reçus de vous, y compris les frais de livraison.
</p>
<p>
    Ce remboursement sera fait sans retard excessif et au plus tard quatorze
    jours à compter du jour où nous sommes informés de votre décision de
    rétractation.
</p>
<p>
    Nous procéderons au remboursement en utilisant le même moyen de paiement
    que celui que vous aurez utilisé pour la transaction initiale ; en tout
    état de cause, ce remboursement n'occasionnera pas de frais pour vous. Nous
    pouvons différer le remboursement jusqu'à ce que nous ayons reçu le bien ou
    jusqu'à ce que vous ayez fourni une preuve d'expédition du bien, la date
    retenue étant celle du premier de ces faits
</p>
<p>
    Vous devrez renvoyer ou rendre le bien, à nous-mêmes sans retard excessif,
    au plus tard quatorze jours après que vous nous aurez communiqué votre
    décision de rétractation du présent contrat. Ce délai est considéré
    respecté si vous renvoyez le bien avant l'expiration du délai de quatorze
    jours. Votre responsabilité n'est engagée qu'à l'égard de la dépréciation
    du bien résultant de manipulations autres que celles nécessaires pour
    établir la nature, les caractéristiques et le bon fonctionnement de ce
    bien.
</p>
<p>
    Vous devrez prendre en charge les frais directs de renvoi du bien.
</p>
