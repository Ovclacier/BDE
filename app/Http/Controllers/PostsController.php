     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        
        $post = Post::Create([
            'title' => $request->title, 
            'content' => $request->content, 
            'author_id'=> $request->author
        ]);
        $post->save();
        $posts = Post::all();


        return view('post.showposts', ['posts'=> $posts]);
    }

